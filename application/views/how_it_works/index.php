<h1>Jak działa Majsteria.pl?</h1>
<br />
<h3>Znajduj sprawdzonych fachowców szybko i łatwo</h3>

<p>
Z Majsterią szybko i skutecznie znajdziesz odpowiedniego fachowca do pomocy przy usterce, czy pracach wymagających specjalistycznej wiedzy.
</p>
<p>
Kilka prostych kroków dzieli Cię od rozwiązania swojego problemu:
Pierwsze, co musisz zrobić to zarejestrować u nas swoje konto przy użycia adresu e-mail lub korzystając ze swojego konta na facebooku.
Następym krokiem jest opisanie swojego problemu i publikacja zlecenia, do którego zgłaszać będą się specjaliści z Twojej okolicy.
Krótko po publikacji zlecenia otrzymasz zgłoszenia od osób lub firm chętnych do wykonania zadania. Zgłoszenia zawierać będą wycenę zlecenia, tak abyś mógł wybrać najkorzystniejszą dla siebie ofertę.
Ostatnim krokiem jest wybór fachowca, który podejmie się wykonania oferowanej przez Ciebie pracy. 
Dzięki Majsterii unikasz tracenia czasu na szukanie ekspertów -- to oni odnajdują Ciebie. 
Usiądź wygodnie, zrelaksuj się i pozwól nam zająć się rozwiązaniem Twojego problemu.
</p>
<br />
<h3>Dodatkowe zlecenia dla Ciebie i Twojego biznesu</h3>

<p>
Majsteria to doskonały sposób na szybkie podniesienie dochodów swoich lub swojej firmy.
Jedyne, co musisz zrobić to utworzyć w naszym serwisie konto, które umożliwi Ci zgłaszanie się do zleceń publikowanych na naszej stronie.
Podaj swoje imię i nazwisko (lub nazwę firmy), lokalizację, dane kontaktowe, oraz fach, w którym się specjalizujesz. My będziemy powiadamiać Cię o każdym zleceniu z Twojego regionu, które będzie pasować do Twoich kwalifikacji. Do Ciebie należy zgłoszenie chęci, oraz wycena wykonania zadania. Zgłoszenie do zlecenia nie jest zobowiązujące, a wszelkie szczegóły wykonania ustalasz ze zleceniodawcą, który będzie się z Tobą kontaktował osobiście.  
</p>


