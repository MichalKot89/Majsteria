<h1>Witamy w serwisie Majsteria.pl</h1>

<p>Celem naszego działania jest ułatwienie błyskawicznego kontaktu z fachowcami zajmujacymi się pracami budowlanymi, naprawami i innym rzemiosłem.
Majsteria zrzesza różnorodnych specjalistów z całej Polski w ponad 50 kategoriach! Znajdziesz tu pomoc profesjonalnego hydraulika, ogrodnika czy nawet inżyniera budownictwa. 
Działanie serwisu jest proste i przejrzyste -- Ty publikujesz zlecenie wybierając konkretną dziedzinę, a my powiadamiamy najlepszych fachowców w Twojej okolicy. Bezpłatnie!
</p>
<p>
Decyzja o wyborze eksperta i przyznaniu zlecenia wciąż należy do Ciebie.<br />
Dzięki Majsterii masz pewność, że powierzone zadanie zostanie wykonane szybko i profesjonalnie. Z nami oszczędzasz czas, wybierasz najkorzystniejszą ofertę spośród wielu, a także unikasz nieuczciwych osób podających się za specjalistów.
Każda firma i osoba prywatna oferująca u nas swoje usługi jest oceniana przez użytkowników, którzy skorzystali z ich oferty. Ty także będziesz mógł wystawić ocenę osobie lub firmie, którą zatrudnisz do wykonania swojego zlecenia.
</p>
<p>
Majsteria to także doskonała forma na reklamę swoich usług jeśli prowadzisz biznes lub jesteś wykwalifikowanym specjalistą. Codziennie pojawia się u nas kilkaset nowych zleceń, które czekają na Twoje zgłoszenie. Bez żadnych dodatkowych kosztów możesz zwiększyć obroty swojej firmy, albo zwyczajnie zapewnić sobie dodatkowy dochód.
</p>
<p>
Nasz zespół Pomocy Technicznej chętnie udzieli odpowiedzi na wszelkie pytania, a także pomoże zapoznać się z serwisem.
</p>
<p>
Nie trać czasu i oddaj swoje problemy w ręce specjalistów!
</p>



